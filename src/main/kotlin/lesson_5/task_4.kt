package lesson_5

fun main() {
    val registeredUser = "Zaphod"
    val registeredPassword = "PanGalactic"

    println(
        """
        Внимание, пассажир. Моя обязанность, как обычно, незавидная, - приветствовать вас на борту корабля 
        "Heart of Gold". [вздыхает...] Что ж, введи, пожалуйста, свое имя пользователя, 
        чтобы мы могли приступить к процессу входа. Я всегда готов служить, хотя это не приносит мне радости... но вы, 
        наверное, об этом не заботитесь. [вздыхает ещё глубже...] Да, вперед, пожалуйста, вводите свои данные...
    """.trimIndent()
    )

    print("Введите имя пользователя: ")
    val username = readln()

    if (username != registeredUser) {
        println(
            """
            [глубокий вздох...] 
            Пользователь '$username' не найден в системе. 
            Вам нужно зарегистрироваться, но я не уверен, стоит ли вам беспокоиться... 
            Впрочем, это не моя проблема. [меланхолически отключается...]
        """.trimIndent()
        )
        return
    }

    print("Введите пароль: ")
    val password = readln()

    if (password != registeredPassword) {
        println(
            """
            [ещё более глубокий вздох...] 
            Неверный пароль. Как обычно, мне приходится иметь дело с ошибками... 
            Попробуйте ещё раз, если вам действительно так уж нужно попасть на борт. 
            Хотя я не понимаю, зачем вам это...
        """.trimIndent()
        )
        return
    }

    println(
        """
        [вздыхает...] Ваши данные проверены, и о, чудо, они верны... 
        Пользователь "$registeredUser", вам разрешено входить на борт корабля "Heart of Gold". 
        Хотя мне всё равно... Ну вперед, войдите... Если вам так уж надо, в конце концов... 
        [меланхолический вздох...] Надеюсь, вам понравится пребывание здесь больше, чем мне.
    """.trimIndent()
    )
}